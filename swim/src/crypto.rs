#[cfg(test)]
mod tests {
    use super::*;

    fn test_key(seed: u8) -> SwimKey {
        [seed; 32]
    }

    const PLAINTEXT: &[u8] = b"hello SWIM";

    #[test]
    fn encrypt_then_decrypt_round_trip() {
        let key = test_key(0x42);
        let ciphertext = encrypt(&key, PLAINTEXT);
        let recovered = decrypt(&key, &ciphertext).unwrap_or_else(|_| std::process::abort());
        assert_eq!(recovered, PLAINTEXT, "decrypted payload must match original");
    }

    #[test]
    fn wrong_key_fails() {
        let key_a = test_key(0x01);
        let key_b = test_key(0x02);
        let ciphertext = encrypt(&key_a, PLAINTEXT);
        assert!(
            matches!(decrypt(&key_b, &ciphertext), Err(CryptoError::AuthFailed)),
            "wrong key must produce AuthFailed"
        );
    }

    #[test]
    fn tampered_ciphertext_fails() {
        let key = test_key(0x07);
        let mut ciphertext = encrypt(&key, PLAINTEXT);
        // Flip a bit in the ciphertext body (past the 17-byte header).
        // Using get_mut to satisfy indexing_slicing lint.
        if let Some(b) = ciphertext.get_mut(20) {
            *b ^= 0xFF;
        }
        assert!(
            matches!(decrypt(&key, &ciphertext), Err(CryptoError::AuthFailed)),
            "tampered ciphertext must produce AuthFailed"
        );
    }

    #[test]
    fn plaintext_packet_rejected_bad_magic() {
        let key = test_key(0x99);
        // Use a packet that is >= OVERHEAD bytes long but does not start with the
        // GRID magic header.  Packets shorter than OVERHEAD return TooShort first.
        let plaintext_packet = b"not encrypted foca bytes --- long enough foca packet here";
        assert!(
            plaintext_packet.len() >= OVERHEAD,
            "test input must be long enough to reach the magic check"
        );
        assert!(
            matches!(decrypt(&key, plaintext_packet), Err(CryptoError::BadMagic)),
            "plaintext packet long enough for magic check must produce BadMagic"
        );
    }

    #[test]
    fn short_plaintext_packet_rejected_too_short() {
        let key = test_key(0x99);
        let short_packet = b"short";
        assert!(
            matches!(decrypt(&key, short_packet), Err(CryptoError::TooShort)),
            "short plaintext packet must produce TooShort"
        );
    }

    #[test]
    fn too_short_packet_fails() {
        let key = test_key(0x11);
        assert!(
            matches!(decrypt(&key, &[0_u8; 10]), Err(CryptoError::TooShort)),
            "short packet must produce TooShort"
        );
    }

    #[test]
    fn unknown_version_fails() {
        let key = test_key(0x55);
        let mut ciphertext = encrypt(&key, PLAINTEXT);
        if let Some(b) = ciphertext.get_mut(4) {
            *b = 0xFF;
        }
        assert!(
            matches!(decrypt(&key, &ciphertext), Err(CryptoError::UnknownVersion(0xFF))),
            "unknown version must produce UnknownVersion"
        );
    }

    #[test]
    fn empty_plaintext_round_trips() {
        let key = test_key(0x00);
        let ciphertext = encrypt(&key, b"");
        let recovered = decrypt(&key, &ciphertext).unwrap_or_else(|_| std::process::abort());
        assert!(recovered.is_empty(), "empty plaintext decrypts to empty");
    }

    #[test]
    fn nonces_differ_across_encryptions() {
        let key = test_key(0xAB);
        let c1 = encrypt(&key, PLAINTEXT);
        let c2 = encrypt(&key, PLAINTEXT);
        let nonce1 = c1.get(5..17).unwrap_or_else(|| std::process::abort());
        let nonce2 = c2.get(5..17).unwrap_or_else(|| std::process::abort());
        assert_ne!(nonce1, nonce2, "repeated encryptions must produce distinct nonces");
        assert_eq!(decrypt(&key, &c1).unwrap_or_else(|_| std::process::abort()), PLAINTEXT);
        assert_eq!(decrypt(&key, &c2).unwrap_or_else(|_| std::process::abort()), PLAINTEXT);
    }

    #[test]
    fn ciphertext_does_not_contain_plaintext() {
        let key = test_key(0xDE);
        let plaintext = b"secret provider state";
        let ciphertext = encrypt(&key, plaintext);
        let tag_start = ciphertext.len().saturating_sub(TAG_LEN);
        let body = ciphertext.get(17..tag_start).unwrap_or_else(|| std::process::abort());
        assert!(
            !body.windows(plaintext.len()).any(|w| w == plaintext),
            "ciphertext body must not contain the plaintext"
        );
    }

    #[test]
    fn wrong_magic_byte_produces_bad_magic() {
        let key = test_key(0x13);
        let mut ciphertext = encrypt(&key, PLAINTEXT);
        if let Some(b) = ciphertext.get_mut(0) {
            *b ^= 0x01;
        }
        assert!(
            matches!(decrypt(&key, &ciphertext), Err(CryptoError::BadMagic)),
            "corrupted magic must produce BadMagic"
        );
    }

    #[test]
    fn wire_format_structure() {
        let key = test_key(0x77);
        let ciphertext = encrypt(&key, PLAINTEXT);
        assert_eq!(
            ciphertext.get(..4).unwrap_or_else(|| std::process::abort()),
            b"GRID",
            "first 4 bytes must be magic"
        );
        assert_eq!(
            ciphertext.get(4).copied().unwrap_or_else(|| std::process::abort()),
            ENCRYPTED_VERSION,
            "byte 4 must be version"
        );
        assert_eq!(
            ciphertext.len(),
            OVERHEAD + PLAINTEXT.len(),
            "total length must be OVERHEAD + plaintext length"
        );
    }
}
