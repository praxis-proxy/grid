#[cfg(test)]
mod tests {
    use tokio::sync::mpsc;

    use super::*;

    fn local_id(port: u16) -> NodeId {
        NodeId::new(
            format!("test-{port}"),
            format!("127.0.0.1:{port}")
                .parse()
                .unwrap_or_else(|_| std::process::abort()),
        )
    }

    #[test]
    fn new_creates_node_without_panic() {
        let (tx, _rx) = mpsc::channel(1);
        let id = local_id(19_001);
        let _node = SwimNode::new(id, tx);
    }

    #[test]
    fn handle_data_with_garbage_returns_empty_output() {
        let (tx, _rx) = mpsc::channel(1);
        let mut node = SwimNode::new(local_id(19_002), tx);
        let output = node.handle_data(b"not-swim-data");
        // garbage is an error at the foca level; output is empty (no members to probe)
        assert!(
            output.messages.is_empty(),
            "garbage data should produce no outbound messages"
        );
    }

    #[test]
    fn handle_timer_with_unrecognised_variant_returns_empty() {
        let (tx, _rx) = mpsc::channel(1);
        let mut node = SwimNode::new(local_id(19_003), tx);
        let output = node.handle_timer(TimerEvent::PeriodicAnnounce);
        assert!(output.is_empty(), "non-Token timer variant should produce no output");
    }

    #[test]
    fn announce_to_self_returns_output() {
        let (tx, _rx) = mpsc::channel(1);
        let id = local_id(19_004);
        let mut node = SwimNode::new(id.clone(), tx);
        let output = node.announce(id);
        // Announcing to self is a protocol no-op; foca may or may not produce messages.
        // The important thing is that it does not panic.
        drop(output);
    }

    #[test]
    fn two_nodes_can_be_created_concurrently() {
        let (tx1, _rx1) = mpsc::channel(1);
        let (tx2, _rx2) = mpsc::channel(1);
        let node1 = SwimNode::new(local_id(19_005), tx1);
        let node2 = SwimNode::new(local_id(19_006), tx2);
        drop((node1, node2));
    }
}
